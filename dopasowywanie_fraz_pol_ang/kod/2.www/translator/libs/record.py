class Rekord:
    pol=""
    ang=""
    gender=""
    description=""

    def getPol(self):
        return self.pol
    
    def getAng(self):
        return self.ang

    def getGender(self):
        return self.gender

    def getDescription(self):
        return self.description
 
    def setPol(self,wp):
        self.pol=wp
    
    def setAng(self,wa):
        self.ang=wa

    def setGender(self,wr):
        self.gender=wr

    def setDescription(self,wo):
        self.description =wo
